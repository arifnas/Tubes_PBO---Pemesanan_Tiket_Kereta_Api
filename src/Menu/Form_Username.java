    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        int konfirmasi=JOptionPane.showConfirmDialog (null, "Apakah data akan dihapus ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi==JOptionPane. YES_OPTION) {
            int row = tabel_username.getSelectedRow();
            String username=tabel_username.getValueAt (row, 0).toString(); 
            user.Hapus_Username (username);
            user.tampilDataUsername ();
        }
    }//GEN-LAST:event_btn_hapusActionPerformed
    
       
