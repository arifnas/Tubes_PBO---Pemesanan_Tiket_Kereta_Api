    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        String username=txt_username.getText ();
        String password=txt_password.getText(); 
        if (txt_username.equals("")) {
            JOptionPane.showMessageDialog (null, "Username harus diisi");
            txt_username.requestFocus();
        }
        else if (txt_password.getText().isEmpty()) { 
            JOptionPane.showMessageDialog (null, "Password harus diisi");
            txt_password.requestFocus ();
        }
        else{
            if (edit==false) {
                user.Simpan_Username (username, password);
            }
            else{
                user.Edit_Username (username, password);
            }
        }
        
        edit=false;
        user.tampilDataUsername ();
        txt_username.setText("");
        txt_password.setText("");
        txt_username.requestFocus();
    }//GEN-LAST:event_btn_simpanActionPerformed

