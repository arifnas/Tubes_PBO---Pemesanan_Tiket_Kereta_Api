    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username=txt_username.getText();
        String password="";
        String getpass=txt_password.getText();
        password=new String(getpass);
        if(log.CekLogin(username, password)==true){
            dispose();
            Menu_Utama menu=new Menu_Utama (null, rootPaneCheckingEnabled);
            menu.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Username dan Password tidak cocok");
            txt_username.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

